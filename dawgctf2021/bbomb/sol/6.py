intList = [64,0x77,0x23,0x91,0xb0,0x72,0x82,0x77,0x63,0x31,0xa2,0x72,0x21,0xf2,0x67,0x82,0x91,0x77,0x26,0x91,0x0,0x33,0x82,0xc4]
finalAns = ""
for n in intList:
    n = n ^ 100
    for i in range(1,200):
        ans = (i << 4) | i >> 4
        ans =  int(hex(ans)[-2:],16)
        if ans == n:
            finalAns += chr(i)

print(finalAns)


