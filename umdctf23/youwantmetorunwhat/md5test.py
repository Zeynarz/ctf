from hashlib import md5

string = [0] * 16
for a in range(0,0x80):
    for b in range(0,0x80):
        for c in range(0,0x80):
            for d in range(0,0x80):
                string[12] = a
                string[13] = b
                string[14] = c
                string[15] = d
                md5sum = md5(bytes(string)).hexdigest()
                if md5sum[:3] == "000":
                    if md5sum[-3:] == "000":
                        print(string,md5sum)


